    /**
     * M√©thode qui permete d'afficher tous les produits
     */
    public void afficherProduit() {
        //Etape de connexion temporaire √† notre base de donn√©es (Modification future avec DaoSIO
        DaoSIO.getInstance();

        //Cr√©ation de l'objet de type DefaultTableModel qui sera li√© √† notre jTable des clients
        DefaultTableModel defaultTableModel = new DefaultTableModel();
        tablePrd = new JTable(defaultTableModel);
        tablePrd.setPreferredScrollableViewportSize(new Dimension(600, 600));
        tablePrd.setFillsViewportHeight(true);

        //Ajout des colonnes √† notre jTable
        defaultTableModel.addColumn("idProduit");
        defaultTableModel.addColumn("nom produit");
        defaultTableModel.addColumn("tarif produit");
        defaultTableModel.addColumn("stock produit");
        defaultTableModel.addColumn("description produit");
        defaultTableModel.addColumn("photo produit");
        defaultTableModel.addColumn("Popularite / Evaluation");
        try {

            ResultSet resultSet = DaoSIO.getInstance().requeteSelection("SELECT * FROM produit"); //Selection, R√©cup√©ration de tous nos clients issus de notre base de donn√©es SQL de la table client 

            while (resultSet.next()) {

                //R√©cup√©ration de nos tuples dans notre table client de notre base de donn√©e MySQL
                String idPrd = resultSet.getString(1); //on r√©cup√®re nos tuples du champs idProduit
                String nomPrd = resultSet.getString(2); //on r√©cup√®re nos tuples du champs nomProduit
                String tarifPrd = resultSet.getString(3); //on r√©cup√®re nos tuples du champs tarifProduit
                String stockPrd = resultSet.getString(4); //on r√©cup√®re nos tuples du champs stockProduit
                String descPrd = resultSet.getString(5); //on r√©cup√®re nos tuples du champs descriptionProduit
                String photoPrd = resultSet.getString(6); //on r√©cup√®re nos tuples du champs photo Produit
                String evaluationPrd = resultSet.getString(5); //on r√©cup√®re nos tuples du champs evaluation Produit

                defaultTableModel.addRow(new Object[]{idPrd, nomPrd, tarifPrd, stockPrd, descPrd, photoPrd, evaluationPrd});//On ajoute nos tuples dans les lignes du tableau de notre Table Java
            }
            jTableListeProduit.setModel(defaultTableModel);
        } catch (SQLException throwables) {
        }
    }

